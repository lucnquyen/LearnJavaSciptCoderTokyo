// example tam giac

function calculateTriangleSquare(a, h) {
    return a * h/2;
}

// Tính diện tích tam giác
// chiều cao h =5, cạnh đáy a = 10;
console.log(calculateTriangleSquare(10, 5));

// tính diện tích hình tròn
// bán kính r = 10;
function calculateDiscSqure(r) {
    return r * r * 3.14;
}

var k = calculateDiscSqure(10);
console.log(k);
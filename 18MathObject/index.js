// Math objects in JSs
// Math.PI
// Math.ceil(number) làm tròn lên
// Math.floor(number) làm tròn xuống
// Math.round(number)
// Math.max(x1, x2, ...)
// Math.min(x1, x2, ...)
// Math.random()
// GG key: Mozilla math Object

function discArea(r) {
    return r * r * Math.PI;
}
console.log(discArea(1));

console.log(9.2);
console.log('ceil ',Math.ceil(9.2));
console.log('floor ', Math.floor(9.2));

console.log('max ',Math.max(1,10));
console.log('min ',Math.min(1,10));

console.log(Math.random());
